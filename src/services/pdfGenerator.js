// Public API
// ---------------------------------------------------------------------------

/**
 * Generates a PDF tax summary from a full tax calculation result.
 * Saves the PDF to a unique temp file and returns the file path.
 *
 * The caller MUST delete the file after use. No user PII is written to the PDF.
 *
 * @param {import('./taxCalculator.js').FullTaxCalculation} taxResult - Full calculation result
 * @param {string} [language='en'] - User's language code (reserved for future i18n of PDF)
 * @returns {Promise<string>} Absolute path to the generated PDF temp file
 * @throws {Error} If PDF generation fails (caller should handle and send text fallback)
 *
 * @example
 * const filePath = await generateTaxSummaryPdf(result, 'en');
 * // ... send filePath via WhatsApp ...
 * await unlink(filePath); // caller cleans up
 */
async function generateTaxSummaryPdf(taxResult, language = 'en') {
  const filePath = createTempFilePath();

  logger.info('Generating tax summary PDF', { language });

  const doc = new PDFDocument({
    // Use asymmetric margins: the larger bottom margin reserves space for the per-page
    // footnote, preventing PDFKit from flowing content into that zone.
    margins: { top: PDF_MARGIN, bottom: PDF_BOTTOM_MARGIN, left: PDF_MARGIN, right: PDF_MARGIN },
    size: 'A4',
  });

  // Attach the footnote to every new page PDFKit adds (pages 2, 3, …).
  // The cursor is reset inside writePageFootnote so content continues from the top.
  doc.on('pageAdded', () => writePageFootnote(doc));

  writeDocumentHeader(doc);
  writeFilingDetailsSection(doc);
  writeIncomeSummary(doc, taxResult);
  writeDeductionsSection(doc, taxResult);
  writeTaxCalculation(doc, taxResult);
  writeResultSection(doc, taxResult);
  writeNextStepsSection(doc);
  writeDeclarationSection(doc);

  // Write the footnote on page 1 manually — the 'pageAdded' event does not fire
  // for the first page (PDFKit creates it synchronously in the constructor).
  writePageFootnote(doc);

  await savePdfToFile(doc, filePath);

  logger.info('Tax summary PDF generated successfully');
  return filePath;
}

export { generateTaxSummaryPdf };
